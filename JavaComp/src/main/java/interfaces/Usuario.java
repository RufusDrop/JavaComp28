    String nombre=panelUsuario.getjTextFieldNombre();
    String correo=panelUsuario.getjTextFieldCorreoElectronico();
    String clave=panelUsuario.getjTextFieldClave();
    String direc=panelUsuario.getjTextFieldDireccion();
